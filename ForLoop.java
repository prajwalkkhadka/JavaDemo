package Assignment1;

public class ForLoop {

	public static void main(String[] args) {
		// Write a Java Program to Demonstrate the use of For Loops
			
		
		int num = 10;
		int sum = 0;
		
		for(int i = 1; i<= num; i++) {
			sum = sum + i;
		System.out.println("The total number is : " + sum);
		
	
	}

}
}